package colorspace;

/**
 * RGB
 */
public class RGB {
    /**  */
    public final double r;
    /**  */
    public final double g;
    /**  */
    public final double b;

    /**
     * @param r
     *            R
     * @param g
     *            G
     * @param b
     *            B
     */
    public RGB(double r, double g, double b) {
	this.r = r;
	this.g = g;
	this.b = b;
    }

}